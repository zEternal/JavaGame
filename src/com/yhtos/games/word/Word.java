package com.yhtos.games.word;

public class Word {

	private String word;
	private int x;
	private int y;
	private int addY;
	
	
	public int getAddY() {
		return addY;
	}
	public void setAddY(int addY) {
		this.addY = addY;
	}
	public String getWord() {
		return word;
	}
	public void setWord(String word) {
		this.word = word;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
	
	
}
